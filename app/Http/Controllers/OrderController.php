    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(User $user)
    {
        $order = new Order();
        $order->user_id = $user->id;
        $order->totalPrice = 0;
        $order->paid=false;
        $order->save();

        return $order;
    }

